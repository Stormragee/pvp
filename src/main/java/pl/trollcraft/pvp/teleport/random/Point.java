package pl.trollcraft.pvp.teleport.random;

public class Point {

    private int x;
    private int z;

    public int getX() {
        return x;
    }

    public int getZ() {
        return z;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setZ(int z) {
        this.z = z;
    }
}
